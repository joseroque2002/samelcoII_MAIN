-- ==========================================
-- 4. reports
-- ==========================================
CREATE TABLE IF NOT EXISTS public.reports (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

ALTER TABLE public.reports
  ADD COLUMN IF NOT EXISTS updated_at timestamptz not null default now(),
  ADD COLUMN IF NOT EXISTS full_name text,
  ADD COLUMN IF NOT EXISTS contact text,
  ADD COLUMN IF NOT EXISTS location_text text,
  ADD COLUMN IF NOT EXISTS issue_type text,
  ADD COLUMN IF NOT EXISTS description text,
  ADD COLUMN IF NOT EXISTS latitude float8,
  ADD COLUMN IF NOT EXISTS longitude float8,
  ADD COLUMN IF NOT EXISTS source text,
  ADD COLUMN IF NOT EXISTS municipality text,
  ADD COLUMN IF NOT EXISTS barangay text,
  ADD COLUMN IF NOT EXISTS status text not null default 'pending',
  ADD COLUMN IF NOT EXISTS resolved_at timestamptz,
  ADD COLUMN IF NOT EXISTS is_urgent boolean default false,
  ADD COLUMN IF NOT EXISTS queue_number bigint,
  ADD COLUMN IF NOT EXISTS assigned_team text;

UPDATE public.reports
SET assigned_team = nullif(btrim(assigned_team), '')
WHERE assigned_team IS DISTINCT FROM nullif(btrim(assigned_team), '');

INSERT INTO public.teams (name, is_active)
SELECT DISTINCT nullif(btrim(assigned_team), ''), true
FROM public.reports
WHERE nullif(btrim(assigned_team), '') IS NOT NULL
ON CONFLICT (name) DO UPDATE
SET is_active = EXCLUDED.is_active;

