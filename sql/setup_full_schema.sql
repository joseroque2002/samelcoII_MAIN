-- ==========================================
-- 2. customer_users (User's "users" table)
-- ==========================================
CREATE TABLE IF NOT EXISTS public.customer_users (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- Ensure all columns exist
ALTER TABLE public.customer_users
  ADD COLUMN IF NOT EXISTS full_name text,
  ADD COLUMN IF NOT EXISTS email text,
  ADD COLUMN IF NOT EXISTS account_number text,
  ADD COLUMN IF NOT EXISTS contact_number text,
  ADD COLUMN IF NOT EXISTS municipality text,
  ADD COLUMN IF NOT EXISTS barangay text,
  ADD COLUMN IF NOT EXISTS password_hash text,
  ADD COLUMN IF NOT EXISTS is_active boolean not null default true,
  ADD COLUMN IF NOT EXISTS last_login_at timestamptz;

-- Unique constraints
DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_indexes WHERE indexname = 'customer_users_email_unique_idx') THEN
        CREATE UNIQUE INDEX customer_users_email_unique_idx ON public.customer_users (lower(email));
    END IF;
    
    IF NOT EXISTS (SELECT 1 FROM pg_indexes WHERE indexname = 'customer_users_account_number_unique_idx') THEN
        CREATE UNIQUE INDEX customer_users_account_number_unique_idx ON public.customer_users (account_number) WHERE nullif(btrim(account_number), '') IS NOT NULL;
    END IF;
END
$$;

-- Enable RLS
ALTER TABLE public.customer_users ENABLE ROW LEVEL SECURITY;

